package local.stattaker.model;

public class TimeDb
{
	private String id;
	private int timeIn;
	private int timeOut;
	public String getId()
	{
		return id;
	}
	public void setId(String id)
	{
		this.id = id;
	}
	public int getTimeIn()
	{
		return timeIn;
	}
	public void setTimeIn(int timeIn)
	{
		this.timeIn = timeIn;
	}
	public int getTimeOut()
	{
		return timeOut;
	}
	public void setTimeOut(int timeOut)
	{
		this.timeOut = timeOut;
	}

}
